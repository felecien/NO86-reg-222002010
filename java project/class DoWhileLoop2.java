// Java Program to Illustrate Do-while Loop

// Class
class DoWhileLoop2
{
	public static void main(String a[])
    {
		int y=21, sum=0;//declaration of class

		do {

			sum+= y;
			y--;
		}

	
		while (y>10);

	
		System.out.println("The Summation is: " + sum);//result of class or programing
	}
}
